        String idadeString = idadeTextField.getText();
        int idade = Integer.parseInt(idadeString);
        String sexo = sexoTextField.getText();
        
        var usuario = new Usuario(nome, idade, sexo);                
        var usuarioDAO = new UsuarioDAO();
